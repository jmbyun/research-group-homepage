<p align=right style='text-align:right'><span lang=EN-US>NIPS Workshop on <a href="http://biglearn.org">Big Learning</a>, 2012.</span></p>

<p><span lang=EN-US><o:p>&nbsp;</o:p></span></p>

<p><b style='mso-bidi-font-weight:normal'><span lang=EN-US>Abstract<o:p></o:p></span></b></p>

<p><span
lang=EN-US>A major obstacle in using Latent Dirichlet Allocation (LDA) [1] is the amount of time it takes for inference, especially for a dataset that starts out large and expands quickly, such as a corpus of blog posts or online news articles. Recent developments in distributed inference algorithms for LDA, as well as minibatchbased online learning algorithms [2] have offered partial solutions for problem. In this paper, we propose a distributed online learning algorithm for LDA for dealing with both aspects of this problem at once. We apply our learning algorithm to a corpus of Twitter conversations and show that it achieves the same model fit within a much shorter learning time.</span></p>

